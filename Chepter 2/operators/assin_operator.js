// = assignment operator
var a = 10; // assigns the value 10 to variable a
var b = 20; // assigns the value 5 to variable b

// a = a + b; // adds b to a and assigns the result to a
a += b;
console.log(a); // 15

a -= b;
console.log(a); // 10

a *= b;
console.log(a); // 50

a /= b;
console.log(a); // 10

a %= b;
console.log(a); // 0
