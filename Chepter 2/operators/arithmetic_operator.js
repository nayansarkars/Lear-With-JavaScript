// Arithmetic Operators
// + Addition
// - Subtraction
// * Multiplication
// / Division
// % Modulus
// ** Exponentiation
// -- Decrement


// % Modulus
var a = 10;
var b = 3;
console.log(a % b);


// ** Exponentiation (2 types)
// Pre Incremental(++a) - Post Incremental (a++)
// Pre Incremental
console.log(++a); // 11
// Post Incremental
console.log(a++); // 11
console.log(a); // 12



// -- Decrement
console.log(--b); // 2
console.log(b--); // 2
console.log(b); // 1
